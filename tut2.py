# Rules for creating variables

# 1. Variable name can not start with a number
# 2. Variable name can only contain alpha numeric characters and underscore
# 3. Variable name can not contain spaces
# 4. Variable name can not be a keyword
# 5. Variable names should be lower case
# 6. Variable names should be descriptive
# 7. Variable names can not contain special characters

