# Escape sequence characters
string = "I am a chad\nI'm also a sigma" # new line
string2 = "I am a chad\nI'm also a \"sigma\"" # literal quoting

print(string, string2)

# 18. tab and backslash escape sequences
string3 = "I am a chad\tI'm also a sigma" # tab space
string4 = "This is a backslash: \\" # backslash escape

print(string3, string4)
